function definedEntries(update) {
  return Object.entries(update).filter(([, value]) => value !== undefined);
}

async function run() {
  const { default: pool } = await import('../util/db.js');
  const client = await pool.connect();

  try {
    await client.query('BEGIN');

    const { rowCount } = await client.query(
      'UPDATE pages SET title = $1, description = $2 WHERE slug = $3',
      [
        HANDWERKER_PAGE_UPDATE.page.title,
        HANDWERKER_PAGE_UPDATE.page.description,
        HANDWERKER_PAGE_UPDATE.slug
      ]
    );

    if (rowCount !== 1) {
      throw new Error(`Expected one handwerker page, updated ${rowCount}`);
    }

    for (const [id, update] of Object.entries(HANDWERKER_PAGE_UPDATE.components)) {
      const entries = definedEntries(update);
      if (!entries.length) continue;

      const assignments = entries.map(([key], index) => `${key} = $${index + 1}`);
      const values = entries.map(([, value]) => value);
      values.push(Number(id));

      const result = await client.query(
        `UPDATE components SET ${assignments.join(', ')} WHERE id = $${values.length}`,
        values
      );

      if (result.rowCount !== 1) {
        throw new Error(`Expected component ${id}, updated ${result.rowCount}`);
      }
    }

    await client.query('COMMIT');
    console.log(
      `Updated ${HANDWERKER_PAGE_UPDATE.slug}: ${HANDWERKER_PAGE_UPDATE.page.title}`
    );
  } catch (error) {
    await client.query('ROLLBACK');
    throw error;
  } finally {
    client.release();
    await pool.end();
  }
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  run().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
